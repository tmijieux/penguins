#ifndef VEC_H
#define VEC_H

typedef struct vec2 {
    double x, y;
} vec2;
    
typedef struct vec3 {
    double x, y, z;
} vec3;
    
typedef struct vec4 {
    double x, y, z, t;
} vec4;


typedef struct vec2f {
    float x, y;
} vec2f;
    
typedef struct vec3f {
    float x, y, z;
} vec3f;
    
typedef struct vec4f {
    float x, y, z, t;
} vec4f;

#endif //VEC_H
